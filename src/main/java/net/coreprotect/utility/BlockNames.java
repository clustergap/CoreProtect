    /**
     * Get the Chinese name for a block by its material name
     *
     * @param materialName the material name (e.g. "stone", "minecraft:stone")
     * @return the Chinese name, or the original name if not found
     */
    public static String getChineseName(String materialName) {
        if (materialName == null || materialName.isEmpty()) return materialName;

        // Remove namespace prefix
        String key = materialName.toLowerCase(Locale.ROOT);
        if (key.contains(":")) {
            key = key.substring(key.indexOf(':') + 1);
        }

        String name = CHINESE_NAMES.get(key);
        return name != null ? name : materialName;
    }

    /**
     * Register additional Chinese names (for CraftEngine custom blocks)
     */
    public static void registerName(String key, String chineseName) {
        if (key != null && chineseName != null) {
            CHINESE_NAMES.put(key.toLowerCase(Locale.ROOT), chineseName);
        }
    }
